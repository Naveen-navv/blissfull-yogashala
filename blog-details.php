        <h3>The Origins and Philosophy of Ayurveda</h3>
        <p>Ayurveda, often referred to as the "science of life," is a holistic healing system that originated in India
            over 5,000 years ago. It is based on the belief that health and wellness depend on a delicate balance
            between the mind, body, and spirit. Central to Ayurvedic philosophy is the concept of the three doshas—Vata,
            Pitta, and Kapha—each representing different elements and energies within the body. An imbalance in these
            doshas is believed to be the root cause of illness and stress.</p>
        <br>
        <p>Ayurvedic massage, or Abhyanga, is a therapeutic practice that involves the use of warm herbal oils tailored
            to an individual's dosha type. This ancient technique aims to balance the doshas, promote relaxation, and
            rejuvenate the entire being.</p>
